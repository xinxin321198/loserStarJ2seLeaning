public class Operator {

	public static void main(String[] args) {
		//位运算
		/*  ~取反
		 *  & 按位与
		 *  |按位或
		 *  ^按位异或
		 *  << 左移运算符
		 *  >>友谊运算符
		 *  
		 *  右移一位相当于除2取商
		 *  左移一位相当于乘2
		 */
		int a = 3*2*2 ;
		int b = 3<<2 ;
		int c = 12/2/2 ;
		int d = 12>>2;
	}

}
